package For_WhileLoop_Task_5Feb;

public class Natural_Number {

    /// sum of 10 natural number=1+2+3+4+5+...10

    public static void main(String[] args)
    {
        int n=10;
        int sum=0;

        for (int i=1;i<=n;i++)
        {
            sum=sum+i;
            System.out.println("Natural Number is: "+sum);

        }

    }





}

