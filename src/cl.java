public class cl {

    //Finding the Largest of Three Numbers a,b,c using the Ternary Operator.

    public static void main(String[] args) {

        int a=10;
        int b=20;
        int c= 30;

        int largest=a>b?(a>c?a:c):(b>c?b:c);
        System.out.println("Largest Number is "+ largest);
    }











}
